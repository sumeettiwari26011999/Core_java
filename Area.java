//Write a java program to find the area of rectangle and square.

class Area
{
	public static void main(String args[])
	{
		int len=5;
		int bre=10;
		int area_of_rectangle=len*bre; //to find area of rectangle
		int area_of_square=len*len;  //to find area of square
		System.out.println("Area of Rectangle=" +area_of_rectangle); 
		System.out.println("Area of Square=" +area_of_square);
	}
}
